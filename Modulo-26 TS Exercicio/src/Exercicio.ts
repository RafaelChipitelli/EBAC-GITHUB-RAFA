// Função Multiplicação:

function multiplicacaoNumeros(numero1: number, numero2: number): number {
    return numero1 * numero2;
}

const resultadoMultiplicacao = multiplicacaoNumeros(5, 10);

console.log(`O resultado da multiplicação é: ${resultadoMultiplicacao}`);


// Função Saudação: 

function saudacao(nome: string): string {
    return `Olá ${nome}`
}

const saudacaoOlá = saudacao('Rafael Chipitelli');

console.log(saudacaoOlá);







